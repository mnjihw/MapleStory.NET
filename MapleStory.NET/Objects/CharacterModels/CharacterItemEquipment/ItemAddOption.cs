namespace MapleStory.NET.Objects.CharacterModels.CharacterItemEquipment;
/// <summary>
/// Item's add option.
/// </summary>
public class ItemAddOption
{
    /// <summary>
    /// STR.
    /// </summary>
    public string? Str { get; set; }
    /// <summary>
    /// DEX.
    /// </summary>
    public string? Dex { get; set; }
    /// <summary>
    /// INT.
    /// </summary>
    public string? Int { get; set; }
    /// <summary>
    /// LUK.
    /// </summary>
    public string? Luk { get; set; }
    /// <summary>
    /// Max HP.
    /// </summary>
    public string? MaxHp { get; set; }
    /// <summary>
    /// Max MP.
    /// </summary>
    public string? MaxMp { get; set; }
    /// <summary>
    /// Attack power.
    /// </summary>
    public string? AttackPower { get; set; }
    /// <summary>
    /// Magic power.
    /// </summary>
    public string? MagicPower { get; set; }
    /// <summary>
    /// Defense.
    /// </summary>
    public string? Armor { get; set; }
    /// <summary>
    /// Speed.
    /// </summary>
    public string? Speed { get; set; }
    /// <summary>
    /// Jump.
    /// </summary>
    public string? Jump { get; set; }
    /// <summary>
    /// Boss damage.
    /// </summary>
    public string? BossDamage { get; set; }
    /// <summary>
    /// Damage(%).
    /// </summary>
    public string? Damage { get; set; }
    /// <summary>
    /// All stat(%).
    /// </summary>
    public string? AllStat { get; set; }
    /// <summary>
    /// Equipment level decrease.
    /// </summary>
    public long EquipmentLevelDecrease { get; set; }
}